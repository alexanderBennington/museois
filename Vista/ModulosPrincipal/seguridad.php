<h2>Información de Seguridad</h2>

<p>La seguridad y el bienestar de nuestros visitantes son nuestra máxima prioridad. Aquí encontrarás información importante sobre las medidas de seguridad que hemos implementado para garantizar una visita segura y placentera.</p>

<h3>Medidas de Seguridad</h3>
<p>Nuestro museo cuenta con medidas de seguridad de última generación para proteger tanto a nuestros visitantes como a nuestras valiosas exhibiciones. Esto incluye sistemas de vigilancia, personal capacitado y procedimientos de emergencia bien establecidos.</p>

<h3>Control de Acceso</h3>
<p>Para garantizar la seguridad, todos los visitantes deben pasar por un control de acceso. Asegúrate de tener tu boleto de entrada listo y sigue las indicaciones del personal para un proceso rápido y eficiente.</p>

<h3>Emergencias y Evacuación</h3>
<p>En caso de una emergencia, sigue las señales de evacuación y las instrucciones del personal. Hemos establecido planes detallados para garantizar una evacuación segura y ordenada en cualquier situación inesperada.</p>

<h3>Asistencia Médica</h3>
<p>Contamos con personal capacitado y áreas designadas para brindar asistencia médica en caso de necesidad. Si tienes alguna preocupación de salud, no dudes en informar al personal para recibir la asistencia adecuada.</p>

<h3>Normas de Comportamiento</h3>
<p>Para mantener un ambiente seguro y respetuoso, pedimos a todos los visitantes que sigan las normas de comportamiento establecidas. Esto incluye no tocar las exhibiciones, respetar las áreas restringidas y mantener un comportamiento adecuado en todo momento.</p>

<h3>Contacto de Emergencia</h3>
<p>Guarda nuestro número de contacto de emergencia en tu teléfono y utilízalo en caso de necesitar ayuda urgente durante tu visita. Estamos aquí para garantizar tu seguridad y bienestar.</p>

